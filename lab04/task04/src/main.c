// Обчислення значення y для графіку y(х) при заданому числі х.
int main() {
        const float x = -5 ; // Число х
        float y ; // Змінна у
        // Заданий графік:
        if (x <= -1) {
                y = -1 / x ;
        }
        if (x > -1 && x < 1) {
                y = x * x ;
        }    
        if (x > 1) { 
                y = 1 ;
        }           
        return 0 ;
}
