// Розставити числа від меншого до найбільшого
int main() {
        int k = 4 ; // Перше число
        int m = 5 ; // Друге число
        int n = 1 ; // Третє число
        int flag = 0 ; // Флаг чисел
        // Порівняння першого і другого чисел
        if (k > m) {
                flag = k ;
                k = m ;
                m = flag ;  
        }
        // Порівняння другого і третього чисел
        if (m > n) {
                flag = m ;
                m = n ;
                n = flag ; 
        }
        // Порівняння першого і другого чисел х2
        if (k > m) {
                flag = k ;
                k = m ;
                m = flag ; 
        }
        return 0 ;
}
